package domesticfinancesystem.regularservice;

/**
 *
 * @author sneha
 */
public class Item {
    private int id;
    private String itemName;
    private float qty;
    private float price;
    

    public Item(int id, String itemName,float qty,float price) {
        this.id = id;
        this.itemName = itemName;
        this.qty = qty;
        this.price = price;
    }

    public float getQty() {
        return qty;
    }

    public void setQty(float qty) {
        this.qty = qty;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }
    
    
    
}
