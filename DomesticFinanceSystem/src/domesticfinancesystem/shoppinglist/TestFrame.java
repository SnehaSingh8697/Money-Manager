    private int noOfDays(Date startDate, Date endDate)
    {
        System.out.println(""+endDate.getTime());
        System.out.println(""+startDate.getTime());
        return (int)(Math.ceil((endDate.getTime() - startDate.getTime())/(double)(24 * 3600 * 1000)));
    }
    
